/*
 *convert_text_graph
 *  DSCRIPTION:Given a string, produce a image of the string.
 *			the string will in the center of the image
 *  INPUT: a pointer to the string and the mode
 *  OUTPUT: None
 *  RETURN VALUE: a pointer to a buffer includes the pixel of the image
 *				the structure is the same as the screen in modex (4 planes)
 *  SIDE EFFECTS: None
 */
void convert_text_graph(const char* input_str, int mode)
{
	/*begin_point calculates from which points, text shows*/
	int begin_point;
	/* Row index and column index*/
	int r_index, c_index;
	/*image with 320 columns and 18 lines*/
	int image_size = 320*18;
	
	int bitmask;
	/*declare a new string the same as input, the argument is const and cannot be modified*/
	char input_str_copy [strlen(input_str)];
	strcpy(input_str_copy, input_str);		
	
	/*The image is made to be black intially */
	if(mode == 0){
		memset(text_image, 0x05, image_size);
	}
	else if(mode == 3){
		memset(text_image, 0x05, image_size);
		return;
	}
	else if(mode == 2 && input_str_copy[strlen(input_str)-1] != '_'){
		strcat(input_str_copy, "_");
	}
		
	// Determine different point to begin based on the mode value
	switch(mode)
	{
		case 0:
			begin_point = (320 - 8 * strlen(input_str_copy))/2;
			break;
		case 1:
			begin_point = 0;
			break;
		case 2:
			begin_point = 320 - 8 * strlen(input_str_copy);
			break;
	}
	
	int column_begin_for_char;		/*number of blank columns*/
	int p_off;                      /*offset of plane*/
	int text_char;					/*ASCII code of the specific */
	int char_index;					/*index of the character in the input_str_copy*/
	
	/*The part to draw the character*/
	for(char_index = 0; char_index < strlen(input_str_copy); char_index++)
	{
		text_char = (int) input_str_copy[char_index];
		column_begin_for_char = begin_point + 8 * char_index;
		for(r_index = 1; r_index < 17; r_index++)
		{
			bitmask = 0x80;
			for(column_index = 0; column_index < 8; column_index++, bitmask >>= 1)
			{
				/*Check whether the pixel is written*/
				if(font_data[text_char][r_index] & bitmask)
				{
					/* Calculate plane offset of the first pixel and fill in the corrosponding pixel*/
					p_off = column_index & 3;
					text_image[p_off*image_size/4 + r_index*320/4 + (column_begin_for_char + c_index)/4] = 0x30;	
				}	
			}
		}
	}
	
	/*The part to clear the spots in right down corner*/
	for(c_index = 312; c_index < 320; c_index++)
	{
		p_off = column_index & 3;
		text_image[p_off*image_size/4 + 16*320/4 + c_index/4] = 0x05;
	}
	
	return;	
}

